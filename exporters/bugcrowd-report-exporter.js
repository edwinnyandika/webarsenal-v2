const fs = require('fs');
const { getModuleById } = require('../lib/module-catalog');
const { parseArgs } = require('../lib/module-runner');
const { generateReport } = require('../utils/report-templater');

const definition = getModuleById('exporters/bugcrowd-report-exporter');

async function run(argv = process.argv) {
  const { flags } = parseArgs(argv.slice(2));
  const inputPath = flags.input || null;
  const outputPath = flags.output || 'bugcrowd-report.md';

  if (!inputPath) {
    console.log(`Usage: node ${definition.filePath} --input <findings.json> --output <report.md>`);
    return;
  }

  console.log(`[*] Generating Bugcrowd report from ${inputPath}...`);

  try {
    const rawData = fs.readFileSync(inputPath, 'utf8');
    const findings = JSON.parse(rawData);

    if (!Array.isArray(findings) || findings.length === 0) {
      console.log('[-] No findings found in the input JSON.');
      return;
    }

    const primary = findings[0];
    const report = [
      `# Bugcrowd Submission: ${primary.title}`,
      `**Target:** ${primary.target}`,
      `**Vulnerability Type:** ${primary.tool.split('/').pop()}`,
      `**Severity:** ${primary.severity}`,
      '',
      '## Vulnerability Description',
      primary.description || 'A security flaw has been identified that could allow for unauthorized actions or data disclosure.',
      '',
      '## Detailed Steps to Reproduce',
      '1. Start the WebArsenal scanning engine:',
      '   ```bash',
      `   node bin/webarsenal run ${primary.tool} --url ${primary.target}`,
      '   ```',
      '2. Review the resulting JSON findings.',
      '3. View the evidence provided below.',
      '',
      '## Proof of Concept / Evidence',
      '### Technical Details',
      '```http',
      primary.evidence?.request || 'N/A',
      '```',
      '',
      '```http',
      primary.evidence?.response || 'N/A',
      '```',
      '',
      '## Remediation Recommendations',
      primary.remediation || 'Enhance the security controls for the identified vulnerability area. Proper input validation and hardened configurations are recommended.',
      '',
      '---',
      '*Report generated by WebArsenal v5.5.0 "Pulse"*'
    ].join('\n');

    fs.writeFileSync(outputPath, report);
    console.log(`[+] Bugcrowd report successfully written to ${outputPath}`);
    return { status: 'ok', reportPath: outputPath };

  } catch (err) {
    console.error(`[-] Error generating Bugcrowd report: ${err.message}`);
  }
}

if (require.main === module) {
  run().catch(console.error);
}

module.exports = { run };
