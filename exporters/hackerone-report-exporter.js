const fs = require('fs');
const { getModuleById } = require('../lib/module-catalog');
const { parseArgs } = require('../lib/module-runner');
const { generateReport } = require('../utils/report-templater');

const definition = getModuleById('exporters/hackerone-report-exporter');

async function run(argv = process.argv) {
  const { flags } = parseArgs(argv.slice(2));
  const inputPath = flags.input || null;
  const outputPath = flags.output || 'hackerone-report.md';

  if (!inputPath) {
    console.log(`Usage: node ${definition.filePath} --input <findings.json> --output <report.md>`);
    return;
  }

  console.log(`[*] Generating HackerOne report from ${inputPath}...`);

  try {
    const rawData = fs.readFileSync(inputPath, 'utf8');
    const findings = JSON.parse(rawData);

    if (!Array.isArray(findings) || findings.length === 0) {
      console.log('[-] No findings found in the input JSON.');
      return;
    }

    // Use the first finding as the primary for the report summary
    const primary = findings[0];
    const report = [
      `# ${primary.title}`,
      `**Severity:** ${primary.severity}`,
      `**Target:** ${primary.target}`,
      '',
      '## Summary',
      primary.description || 'This vulnerability allows for unauthorized actions or information disclosure on the target system.',
      '',
      '## Steps to Reproduce',
      '1. Invoke the WebArsenal scanner with the following command:',
      '   ```bash',
      `   node bin/webarsenal run ${primary.tool} --url ${primary.target}`,
      '   ```',
      '2. Analyze the output findings in the JSON report.',
      '3. Observe the evidence below.',
      '',
      '## Evidence',
      '### Request',
      '```http',
      primary.evidence?.request || 'N/A',
      '```',
      '',
      '### Response',
      '```http',
      primary.evidence?.response || 'N/A',
      '```',
      '',
      '## Impact',
      'This vulnerability could lead to significant data exposure or unauthorized access, impacting the confidentiality and integrity of the target system.',
      '',
      '## Remediation',
      primary.remediation || 'Implement strict input validation and follow best security practices for the identified vulnerability type.',
      '',
      '---',
      '*Report generated by WebArsenal v5.5.0 "Pulse"*'
    ].join('\n');

    fs.writeFileSync(outputPath, report);
    console.log(`[+] HackerOne report successfully written to ${outputPath}`);
    return { status: 'ok', reportPath: outputPath };

  } catch (err) {
    console.error(`[-] Error generating HackerOne report: ${err.message}`);
  }
}

if (require.main === module) {
  run().catch(console.error);
}

module.exports = { run };
